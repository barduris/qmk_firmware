void ACUTE(uint16_t keycode) {
    if (isShiftDown) {
      unregister_code(KC_LSFT);
      register_code(KC_QUOT);
      unregister_code(KC_QUOT);
      register_code(KC_LSFT);
    } else {
      register_code(KC_QUOT);
      unregister_code(KC_QUOT);
    }
    register_code(keycode);
    unregister_code(keycode);
    layer_on(_ISK); // shift seems to mess with this
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        print("mode just switched to qwerty and this is a huge string\n");
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case COLEMAK:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_COLEMAK);
      }
      return false;
      break;
    case DVORAK:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_DVORAK);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case BACKLIT:
      if (record->event.pressed) {
        register_code(KC_RSFT);
        #ifdef BACKLIGHT_ENABLE
          backlight_step();
        #endif
      } else {
        unregister_code(KC_RSFT);
      }
      return false;
      break;
    case PLOVER:
      if (record->event.pressed) {
        #ifdef AUDIO_ENABLE
          stop_all_notes();
          PLAY_SONG(plover_song);
        #endif
        layer_off(_RAISE);
        layer_off(_LOWER);
        layer_off(_ADJUST);
        layer_on(_PLOVER);
        if (!eeconfig_is_enabled()) {
            eeconfig_init();
        }
        keymap_config.raw = eeconfig_read_keymap();
        keymap_config.nkro = 1;
        eeconfig_update_keymap(keymap_config.raw);
      }
      return false;
      break;
    case EXT_PLV:
      if (record->event.pressed) {
        #ifdef AUDIO_ENABLE
          PLAY_SONG(plover_gb_song);
        #endif
        layer_off(_PLOVER);
      }
      return false;
      break;
    case ISK:
      if(record->event.pressed) {
        layer_off(_RAISE);
        layer_off(_LOWER);
        layer_off(_ADJUST);
        layer_on(_ISK);
      } else {
        layer_off(_ISK);
      }
      return false;
      break;
    case KC_LSFT://||KC_RSFT:
      if(record->event.pressed) {
        isShiftDown = true;
      } else {
        isShiftDown = false;
      }
      return true;
      break;
    case KC_ACUTE_A:
      if(record->event.pressed) {
        ACUTE(KC_A);
      }
      return false;
      break;
    case KC_ACUTE_E:
      if(record->event.pressed) {
        ACUTE(KC_E);
      }
      return false;
      break;
    case KC_ACUTE_Y:
      if(record->event.pressed) {
        ACUTE(KC_Y);
      }
      return false;
      break;
    case KC_ACUTE_U:
      if(record->event.pressed) {
        ACUTE(KC_U);
      }
      return false;
      break;
    case KC_ACUTE_I:
      if(record->event.pressed) {
        ACUTE(KC_I);
      }
      return false;
      break;
    case KC_ACUTE_O:
      if(record->event.pressed) {
        ACUTE(KC_O);
      }
      return false;
      break;
  }
  return true;
}